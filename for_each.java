public  class for_each{
    public static void main(String[] args) {
        int num[]={1,23,6,7};
        for (int var : num) {
            System.out.println(var);
        
            
        }
        String lot[]={"one","two"};
        for (String var : lot) {
            System.out.println(var);
        
            
        }
    }
}